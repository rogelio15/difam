    public void calculaPoliticas() {
        double ganancia=0.00;
        if (tabla.getRowCount()>0) {
            for (int i = 0; i < tabla.getRowCount(); i++) {
                int diasDif = Integer.parseInt(tabla.getValueAt(i, 6).toString());
                ganancia = Double.parseDouble(tabla.getValueAt(i, 8).toString());
                System.out.printf("Dias:%s Ganancia:%s",diasDif,ganancia);
                if(diasDif<=15){
                    ganancia = ganancia;
                }else if(diasDif<=22){
                    ganancia = ganancia*0.70;
                }else if(diasDif<=30){
                    ganancia = ganancia*0.50;
                }else if(diasDif<=45){
                    ganancia = ganancia*0.30;
                }else if(diasDif<=60){
                    ganancia = ganancia*0.10;
                }else{
                    ganancia = 0;
                    //frmPrincipal.miConex.actualizaRegistro("venta","IF(cliOAgente=0,cliOAgen=1,'')","idVenta='"+tabla.getValueAt(i, 0).toString()+"'");
                    //Aqui pasar la cuenta diferencial como pago del agente
                }
                tabla.setValueAt(ganancia,i, 9);
            }
        }
        CalculaTotal(9);
    }

    public void CalculaTotal(int col){
        double pagoVenta = 0.00;
        for (int i = 0; i < tabla.getRowCount(); i++) {
            pagoVenta += Double.parseDouble(tabla.getValueAt(i, col).toString());            
        }
        lblTotal.setText(""+pagoVenta);
    }
    public void consulta(String condicion) {
        Vector datos = frmPrincipal.miConex.consDatosParaTablas("V.idVenta,C.razonsocialcliente,V.montoTotalV,V.fechaVenta,ADDDATE(V.fechaVenta,C.limitediascred) as fechaVence,fechaCierre,DATEDIFF(curdate(),ADDDATE(V.fechaVenta,C.limitediascred)) as dias,DATEDIFF('2014-09-29',ADDDATE(V.fechaVenta,C.limitediascred)) as dias1,V.gananciaVEmp", "venta", "V inner join cliente C inner join ruta_agente RA inner join empleado E inner join ruta R ON C.idCliente=V.idCliente and C.idR_A=RA.idR_A and RA.idEmpleado=E.idEmpleado and R.idruta=RA.idruta where " + condicion + " estadoCobro='0' and E.idEmpleado='" + txtID.getText() + "'");
        if (datos.size() > 0) {
            tabla.setModel(new DefaultTableModel(datos, columnas));
            tabla.setAutoCreateRowSorter(true);
            frmPrincipal.packColumn(tabla, 0, 2);

        } else {
            tabla.setModel(new DefaultTableModel(null, columnas));
        }
        calculaPoliticas();
    }
