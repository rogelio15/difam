    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        StringBuilder ventas=new StringBuilder("");
        for (int i = 0; i < tabla.getRowCount(); i++) {
            frmPrincipal.miConex.actualizaRegistro("venta", "estadoCobro='1'", "idVenta='" + tabla.getValueAt(i, 0) + "'");
            ventas.append(tabla.getValueAt(i, 0));
            if(!(i == tabla.getRowCount()-1)){
                ventas.append(",");
            }
            System.out.println("Ventas:"+ventas);
        }
        frmPrincipal.miConex.altaRegistro("pagoEmpleado", "null,"+lblTotal.getText()+",'"+ventas+"',sysdate(),"+txtID.getText());
        HashMap param = new HashMap();
        System.out.println("IDPAGO EMPLEADO: " + frmPrincipal.miConex.ConsultaDato("idPagoE", "pagoEmpleado", " where fechaCobro=sysdate() and idEmpleado='"+txtID.getText()+"'"));
        param.put("idEmp",txtID.getText().toString());//frmPrincipal.miConex.ConsultaDato("idPagoE", "pagoEmpleado", " where fechaCobro=sysdate() and idEmpleado='"+txtID.getText()+"'"));
        param.put("ventas",ventas.toString());
        frmPrincipal.agregaPanel(new panelVistaReportes("pagare",param), new BorderLayout(), null, null);
    }//GEN-LAST:event_btnPagarActionPerformed

